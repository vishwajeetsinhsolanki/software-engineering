# Write a Python program to demonstrate the use of functions from the math module.

import math

n = float(input("Enter number : "))

print(f"Square root : {math.sqrt(int(n))}")
print(f"Floor value : {math.floor(n)}")
print(f"ceiling value : {math.ceil(n)}")
print(f"Factorial : {math.factorial(int(n))}")
print(f"Sine of number : {math.sin(int(n))}")
print(f"Cosine of number : {math.cos(int(n))}")
print(f"Pi value : {math.pi}")
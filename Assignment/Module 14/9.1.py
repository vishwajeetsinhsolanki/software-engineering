#Write a Python program to import the math module and use functions like sqrt(), ceil(), floor().

import math

n1 = int(input("Enter whole number : "))
n2 = float(input("Enter number with fractional part : "))

print(f"sqrt() : {math.sqrt(n1)}")
print(f"ceil() : {math.ceil(n2)}")
print(f"floor() : {math.floor(n2)}")